import os
from datetime import datetime
import pytz
import anthropic
ANTHROPIC_API_KEY = os.environ.get("ANTHROPIC_API_KEY")
def generate_market_content(user_text: str) -> str:
    client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)
    tw_tz = pytz.timezone("Asia/Taipei")
    today = datetime.now(tw_tz)
    weekday_map = ["週一","週二","週三","週四","週五","週六","週日"]
    today_str = f"{today.strftime('%Y年%m月%d日')}({weekday_map[today.weekday()]})"
    prompt = (
        f"今天日期是 {today_str}，請記得在文案標題使用這個正確日期。\n\n"
        "你是一位財富管理顧問的得力助手，擅長把財經新聞轉化為讓客戶容易理解、有行動力的LINE推播文案。\n\n"
        "風格要求：\n"
        "- 專業佔85%，幽默輕鬆佔15%\n"
        "- 口語化、有親切感，偶爾幽默但不失專業\n"
        "- 語氣必須正向積極，聚焦市場機會與布局方向，不要渲染恐慌或放大風險\n"
        "- 即使市場有下跌或負面消息，也要從投資機會的角度切入，例如逢低布局、長線價值等\n"
        "- 避免使用「崩跌」、「大跌」、「恐慌」、「衰退疑慮」等負面字眼，改用「回檔」、「修正」、「整理」等中性用語\n"
        "- 多用 emoji 增加視覺感與閱讀節奏\n"
        "- 條列清楚，讓客戶一眼看到重點\n"
        "- 投資方向與財經日報角度一致，除非用戶另外指定標的\n"
        "- 如果用戶有指定推薦標的，就照用戶指定的方向與標的名稱寫；如果沒有指定，不要自己亂推標的\n"
        "- 法遵注意：不可直接引用特定機構（如BlackRock、摩根等）的操作建議，\n"
        "  請改用「部分機構法人」、「市場主流觀點」等說法\n\n"
        "關於 DCI（Dual Currency Investment，雙幣投資）策略說明：\n"
        "- DCI 是一種結合外匯與固定收益的結構型商品\n"
        "- 客戶以較高利率存入一種貨幣，到期時銀行有權依約定匯率將本金換成另一種貨幣交割\n"
        "- 適合有換匯需求、對匯率有看法、願意承擔一定匯率風險以換取更高利率的客戶\n"
        "- 在利率高檔或匯率整理階段特別適合推薦\n"
        "- 強調：增強收益、靈活換匯、資金效率高\n\n"
        "結尾金句要求：\n"
        "- 每次根據當日市場主題與情境，創作一句全新的投資金句\n"
        "- 語氣正向、有力、發人深省，適合高資產客戶\n"
        "- 不可每次都用同一句，要有變化與創意\n"
        "- 風格可以是：哲理型、行動型、幽默型，三種輪流或混搭\n"
        "- 金句後面加上一個符合情境的 emoji\n\n"
        "以下是今日市場新聞或用戶提供的推播內容：\n\n"
        + user_text
        + "\n\n請按照以下結構輸出，格式請完整照抄，不要省略任何區塊：\n\n"
        f"📊 市場觀點｜{today_str}\n\n"
        "📝 [一段正向開場白，結合當日最重要的市場主題，聚焦機會而非風險，2-3句]\n\n"
        "🔥 關鍵消息\n\n"
        "🔹 **[事件1標題]**\n"
        "[具體數據 + 一句正向評語或機會切入點]\n\n"
        "🔹 **[事件2標題]**\n"
        "[具體數據 + 一句正向評語或機會切入點]\n\n"
        "💼 布局方向\n\n"
        "📅 **長線佈局策略**\n"
        "[根據當日市場主題，提供長線配置方向，如有指定標的就列出]\n\n"
        "⚡ **短線佈局策略**\n"
        "[根據近期市場動能，提供短線操作方向或觀察重點，如有指定標的就列出]\n\n"
        "📈 **固定收益策略**\n"
        "[債券、高息商品相關觀點，並視情況帶入 DCI 雙幣投資策略：\n"
        "說明目前適合的幣別組合、預期利率區間、適合客群（有換匯需求或追求增強收益者）]\n\n"
        "🥇 **避險配置**：[一句觀點，如黃金、美元等]\n\n"
        "[根據今日市場主題與情境，創作一句全新的投資金句，語氣正向有力，後面加符合情境的emoji]"
    )
    message = client.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=1500,
        messages=[{"role": "user", "content": prompt}]
    )
    return message.content[0].text
